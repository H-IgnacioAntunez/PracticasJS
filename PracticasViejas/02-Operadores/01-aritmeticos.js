let a = 5;
let b = 3;

console.log(a+b,"Adicion") //Signo aritmético de adición - Suma
console.log(a-b,"Sustraccion") //Signo aritmético de sustracción - Resta
console.log(a*b,"Producto") //Signo aritmético de producto - Multiplicación
console.log(a/b,"Division") //Signo aritmético de division
console.log(a%b,"Resto") //Signo aritmético porcentaje - El modulo/resto de una división
console.log(a**2,"Potencia") //Signo aritmético potencia

//Incrementar un numero
//En este caso primero se va a sumar 1 a el valor de a y luego se va a imprimir
console.log(++a);
//Ahora vamos a imprimir primero a y luego sumarle 1
console.log(a++);
console.log(a);

//Decrementar un numero
//Lo mismo que previamente, tenemos 2 opciones
//Se resta y se muestra
console.log(--a);
//Se muestra y luego se resta
console.log(a--);
console.log(a);

